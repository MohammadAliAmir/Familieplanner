    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        for(Poll p : polls){
            if(p.getVraag().equals(jList1.getSelectedValue())){
                if(groep.getAdmin().equals(user)){
                polls.remove(p);
                JFrame polllijst = new PollLijst(users, groepen, user, polls, groep);
                setVisible(false);
                polllijst.setVisible(true);
                }
                else{
                    JOptionPane.showMessageDialog(this, "Enkel beheerder kan poll verwijderen", "Famillieplanner", JOptionPane.INFORMATION_MESSAGE, new ImageIcon("pictures/error.png"));
                }
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

