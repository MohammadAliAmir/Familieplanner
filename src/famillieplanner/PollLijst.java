    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if(groep.getAdmin().equals(user)){
        JFrame newPoll = new newPoll(users, groepen, user, polls, groep);
        setVisible(false);
        newPoll.setVisible(true);
        }
        else{
            JOptionPane.showMessageDialog(this, "Enkel beheerder kan poll aanmaken", "Famillieplanner", JOptionPane.INFORMATION_MESSAGE, new ImageIcon("pictures/error.png"));
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        for(Poll p : polls){
            if(p.getVraag().equals(jList1.getSelectedValue()))
            {
                JFrame stempoll = new StemPoll(p, users, groepen, user, polls);
                stempoll.setVisible(true);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        for(Poll p : polls){
            if(p.getVraag().equals(jList1.getSelectedValue())){
                JFrame resultaat = new toonResultaatPoll(p);
                resultaat.setVisible(true);
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

